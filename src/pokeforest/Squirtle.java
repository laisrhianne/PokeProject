package pokeforest;

public class Squirtle extends Pokemon{
	public void surf() {
		System.out.println("Surf!");
		attackPower = 44;
	}

	public void hydroPump() {
		System.out.println("Hydro pump!");
		attackPower = 50;
	}

	public void bubbles() {
		System.out.println("Bubles!");
		attackPower = 38;
	}

	public void doubleSlap() {
		System.out.println("Double slap!");
		attackPower = 18;
	}
}
